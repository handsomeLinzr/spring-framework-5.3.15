	/**
	 * Create a new AbstractAutowireCapableBeanFactory.
	 *
	 * 创建一个新的BeanFactory
	 *
	 */
	public AbstractAutowireCapableBeanFactory() {
		// 默认这里super() 父类的构造函数是空实现
		super();
		ignoreDependencyInterface(BeanNameAware.class);
		ignoreDependencyInterface(BeanFactoryAware.class);
		ignoreDependencyInterface(BeanClassLoaderAware.class);
		if (NativeDetector.inNativeImage()) {
			// graalvm 轻量级vm的内容，这里一般没有
			this.instantiationStrategy = new SimpleInstantiationStrategy();
		}
		else {
			this.instantiationStrategy = new CglibSubclassingInstantiationStrategy();
		}
	}

	/**
	 * Create a new AbstractAutowireCapableBeanFactory with the given parent.
	 * @param parentBeanFactory parent bean factory, or {@code null} if none
	 */
	public AbstractAutowireCapableBeanFactory(@Nullable BeanFactory parentBeanFactory) {
		// 1.设置忽略的三个aware接口 ignoreDependencyInterface，分别是 BeanNameAware、BeanFactoryAware、BeanClassLoaderAware
		// 2.设置 instantiationStrategy = CglibSubclassingInstantiationStrategy
		this();
		// 设置父工厂，单纯Spring的情况下parent是null
		setParentBeanFactory(parentBeanFactory);
	}


